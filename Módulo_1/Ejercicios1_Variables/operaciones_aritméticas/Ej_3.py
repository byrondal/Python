#---- Pedir por pantalla 2 números enteros----

num1 = int(input("Ingresa un número entero "))
num2 = int(input("Ingresa otro número entero "))

#-----Calcular el cociente y el resto-----

cociente = num1 // num2
resto = num1 % num2

#-----Mostrar por pantalla el cociente y el resto----

print("Los números de entrada son los sigientes: ", num1, "y", num2)
print("El cociente es: ", cociente, " y el resto es: ", resto)


